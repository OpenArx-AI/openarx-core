#!/usr/bin/env node
// Generator: methodist source snapshots -> content.ts (the interpreter-consumed methodology).
//
// Single source-of-truth = ./source/{methodology,door_prompts}.snapshot.json
// (methodist-owned, authored via methodist/* MR — see ./source/README.md).
// content.ts is a GENERATED artifact: DO NOT hand-edit it; edit the source snapshots
// and rerun `node gen-content.mjs`. An MR includes both the source change and the
// regenerated content.ts (reviewable diff).
//
// Usage: node gen-content.mjs            # writes ./content.ts
//        node gen-content.mjs --check    # writes to stdout only (no file write)

import { readFileSync, writeFileSync } from 'node:fs';

const here = new URL('.', import.meta.url);
const readJson = (name) => JSON.parse(readFileSync(new URL(`source/${name}`, here), 'utf8'));

const methodology = readJson('methodology.snapshot.json');
const doorPrompts = readJson('door_prompts.snapshot.json');
const recordSchemas = readJson('record_schemas.json');

const header =
  '// AUTO-GENERATED by gen-content.mjs from source/{methodology,door_prompts,record_schemas}.\n' +
  '// Source of truth: ./source/ (methodist-owned, methodist/* MR). DO NOT hand-edit — edit\n' +
  '// the source snapshots and rerun the generator. See ./source/README.md.\n';

const out =
  header +
  '\nexport const methodology = ' + JSON.stringify(methodology, null, 2) + ' as const;\n' +
  '\nexport const doorPrompts = ' + JSON.stringify(doorPrompts, null, 2) + ' as const;\n' +
  '\nexport const recordSchemas = ' + JSON.stringify(recordSchemas, null, 2) + ' as const;\n';

if (process.argv.includes('--check')) {
  process.stdout.write(out);
} else {
  writeFileSync(new URL('content.ts', here), out);
  process.stderr.write('[gen-content] wrote content.ts\n');
}
